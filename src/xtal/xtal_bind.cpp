bool Any_op_in_Array(const AnyPtr& v, const ArrayPtr& values){
	const VMachinePtr& vm = vmachine();
	Xfor(n, values){
		if(raweq(n, v)){
			return true;
		}

		vm->setup_call(1);
		vm->push_arg(v);
		n->rawsend(vm, Xid2(op_eq));
		if(vm->processed() && vm->result()){
			vm->cleanup_call();
			return true;
		}
		vm->cleanup_call();
	}

	return false;
}

